#include "libft.h"

int	ft_putstr_fd(char *s, int fd)
{
	if (s == NULL)
	{
		write(1, "(null)", 6);
		return (6);
	}
	if (!s || fd < 0)
		return (0);
	write(fd, s, ft_strlen(s));
	return (ft_strlen(s));
}
