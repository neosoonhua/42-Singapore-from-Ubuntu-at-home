#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	digits[10];
	int		i;

	if (fd < 0)
		return ;
	if (n == -2147483648)
		write(fd, "-2147483648", 11);
	else if (n == 0)
		write(fd, "0", 1);
	else
	{
		if (n < 0)
		{
			write(fd, "-", 1);
			n *= -1;
		}
		i = 0;
		while (n != 0)
		{
			digits[i++] = n % 10 + '0';
			n = n / 10;
		}
		while (i != 0)
			write(fd, &digits[--i], 1);
	}
}
